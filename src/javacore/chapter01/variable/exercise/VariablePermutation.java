package javacore.chapter01.variable.exercise;

public class VariablePermutation {

    public static void main (String[] args) {

        int a = 10;
        int b = 20;

        System.out.println(a);
        System.out.println(b);

        a = b;
        System.out.println(a);
        a = 10;
        b = a;

        System.out.println(b);


    }



}
